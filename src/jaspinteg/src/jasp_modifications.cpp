
#include "jasp_modifications.h"

#include "conversion.h"


//-----------------------------------------------------------------------------------------------//
//-- JasperModification -------------------------------------------------------------------------//
//-----------------------------------------------------------------------------------------------//

//-----------------------------------------------------------------------------------------------//
JasperModification::JasperModification(const std::string& mod_str)
: m_mod_str(mod_str)
{
}

//-----------------------------------------------------------------------------------------------//
JasperModification::~JasperModification()
{
}

//-----------------------------------------------------------------------------------------------//
const std::string& JasperModification::getModStr() const
{
  return m_mod_str;
}


//-----------------------------------------------------------------------------------------------//
//-- JasperModifications ------------------------------------------------------------------------//
//-----------------------------------------------------------------------------------------------//

//-----------------------------------------------------------------------------------------------//
JasperModifications::JasperModifications()
: m_items()
{
}

//-----------------------------------------------------------------------------------------------//
JasperModifications::~JasperModifications()
{
}

//-----------------------------------------------------------------------------------------------//
void JasperModifications::add(JasperModification* item)
{
  m_items.push_back(JasperModification_Sp(item));
}

//-----------------------------------------------------------------------------------------------//
int JasperModifications::count() const
{
  return m_items.size();
}

//-----------------------------------------------------------------------------------------------//
JasperModificationList::const_iterator JasperModifications::begin() const
{
  return m_items.begin();
}

//-----------------------------------------------------------------------------------------------//
JasperModificationList::const_iterator JasperModifications::end() const
{
  return m_items.end();
}
